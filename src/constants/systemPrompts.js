// 根据预设值拿到真正发送给模型的提示词，未命中时返回空串，让上层决定是否回退到默认行为。
export const getSystemPromptByPreset = (presetValue) => {
  const preset = SYSTEM_PROMPT_PRESETS.find((item) => item.value === presetValue)
  return preset ? preset.prompt : ''
}
